package com.idera.sqlcm.ui.dialogs.addalertruleswizard;

public class AlertRuleConditions {

	private int _conditionId ;
    private int _ruleId ;
    private int _fieldId ;
    private String _matchString ;
    
    
	public int get_conditionId() {
		return _conditionId;
	}
	public void set_conditionId(int _conditionId) {
		this._conditionId = _conditionId;
	}
	public int get_ruleId() {
		return _ruleId;
	}
	public void set_ruleId(int _ruleId) {
		this._ruleId = _ruleId;
	}
	public int get_fieldId() {
		return _fieldId;
	}
	public void set_fieldId(int _fieldId) {
		this._fieldId = _fieldId;
	}
	public String get_matchString() {
		return _matchString;
	}
	public void set_matchString(String _matchString) {
		this._matchString = _matchString;
	}
	
	
	
}
